/*
 * Adds two given integers and prints the result in a fancy way.
 */
public class AddTwo {
	public static void main(String[] args) {
		int a = 3 ;
		int b = 5 ;
		System.out.println(a + " + " + b + " = " + (a + b));
		int c = 9281 ;
		int d = 719 ;
		System.out.println(c + " + " + d + " = " + (c + d));

	}
}
